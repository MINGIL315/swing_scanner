"""한국 호가 단위 정렬 테스트."""
from __future__ import annotations

import pytest

from scanner.kr.quote_align import align_to_tick, get_tick_size


# ---------------------------------------------------------------------------
# get_tick_size — 호가 단위 경계 검증
# ---------------------------------------------------------------------------


class TestGetTickSize:
    @pytest.mark.parametrize(
        "price,expected_tick",
        [
            # 1원 단위 (1,000 미만)
            (1, 1),
            (500, 1),
            (999, 1),
            # 5원 단위 (1,000 ~ 5,000 미만)
            (1_000, 5),
            (3_000, 5),
            (4_999, 5),
            # 10원 단위 (5,000 ~ 20,000 미만)
            (5_000, 10),
            (12_345, 10),
            (19_999, 10),
            # 50원 단위 (20,000 ~ 50,000 미만)
            (20_000, 50),
            (30_000, 50),
            (49_999, 50),
            # 100원 단위 (50,000 ~ 200,000 미만)
            (50_000, 100),
            (100_000, 100),
            (199_999, 100),
            # 500원 단위 (200,000 ~ 500,000 미만)
            (200_000, 500),
            (350_000, 500),
            (499_999, 500),
            # 1,000원 단위 (500,000 이상)
            (500_000, 1_000),
            (1_500_000, 1_000),
            (10_000_000, 1_000),
        ],
    )
    def test_boundary(self, price: float, expected_tick: int) -> None:
        assert get_tick_size(price) == expected_tick


# ---------------------------------------------------------------------------
# align_to_tick — 정렬 결과 검증
# ---------------------------------------------------------------------------


class TestAlignToTick:
    @pytest.mark.parametrize(
        "price,expected",
        [
            # 100원 단위 정렬
            (97_048.5, 97_000.0),     # 100,000원 부근 종목 (예: 삼성전자)
            (97_050.0, 97_100.0),     # half-up (5는 올림)
            (123_456, 123_500.0),
            # 500원 단위 정렬
            (231_127, 231_000.0),
            (231_250, 231_500.0),     # half-up
            (250_499, 250_500.0),     # 0.998 → 1.0
            # 10원 단위 정렬 (half-up)
            (12_345, 12_350.0),
            (12_344, 12_340.0),
            # 50원 단위 정렬
            (24_999, 25_000.0),
            (48_725, 48_750.0),
            (48_724, 48_700.0),
            # 1원 단위 (1,000 미만)
            (500.5, 501.0),
            (999.4, 999.0),
            # 1,000원 단위
            (1_500_000, 1_500_000.0),
            (1_500_499, 1_500_000.0),
            (1_500_500, 1_501_000.0),
        ],
    )
    def test_alignment(self, price: float, expected: float) -> None:
        assert align_to_tick(price) == expected

    def test_zero_returns_zero(self) -> None:
        assert align_to_tick(0) == 0.0

    def test_negative_unchanged(self) -> None:
        """음수는 입력 그대로 반환 (정상 시나리오 아님 — 방어)."""
        assert align_to_tick(-100) == -100.0

    def test_returns_float(self) -> None:
        """반환 타입은 항상 float."""
        result = align_to_tick(50_000)
        assert isinstance(result, float)
